public class Coordenadas {

	private float X;
	private float Y;
	
	public Coordenadas(float x, float y) {
		this.X = x;
		this.Y = y;
	}
	
	public float getX() {
		return X;
	}

	public void setX(float x) {
		X = x;
	}

	public float getY() {
		return Y;
	}

	public void setY(float y) {
		Y = y;
	}

}
